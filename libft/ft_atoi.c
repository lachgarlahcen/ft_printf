int	ft_atoi(const char *str)
{
	int i;
	int res;
	int n;
	int j;

	n = 1;
	i = 0;
	res = 0;
	j = 0;
	while ((str[i] == '\n') || (str[i] == '\t') || (str[i] == '\v')
			|| (str[i] == ' ') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	if (str[i] == '-')
		n = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] != '\0' && ((str[i] >= '0' && str[i] <= '9') ||
				(i == 0 && (str[0] == '-' || str[0] == '+'))))
	{
		res = (res * 10) + (str[i++] - 48);
	}
	res *= n;
	return (res);
}
